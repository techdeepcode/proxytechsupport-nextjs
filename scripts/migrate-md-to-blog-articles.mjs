/**
 * One-time / maintenance: convert content/posts/*.md → content/blog-articles/<slug>/
 * Run from repo root: node scripts/migrate-md-to-blog-articles.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import matter from 'gray-matter';
import { remark } from 'remark';
import remarkGfm from 'remark-gfm';
import remarkHtml from 'remark-html';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const POSTS = path.join(ROOT, 'content/posts');
const OUT = path.join(ROOT, 'content/blog-articles');

function slugFromFilename(file) {
  const base = file.replace(/\.md$/i, '');
  const m = base.match(/^\d{4}-\d{2}-\d{2}-(.+)$/);
  return m ? m[1] : base;
}

function safeIdent(slug) {
  return slug.replace(/-/g, '_');
}

async function mdToHtml(mdBody) {
  const file = await remark().use(remarkGfm).use(remarkHtml).process(mdBody);
  return String(file);
}

/** Hand-written Article.tsx + no body.html — only meta.ts is refreshed from MD. */
const CUSTOM_ARTICLE_SLUGS = new Set(['rag-aimlops-retrieval-augmented-generation-guide']);

function articleTemplate(slug) {
  const slugJson = JSON.stringify(slug);
  return `import fs from 'fs';
import path from 'path';
import BlogArticleShell from '@/components/BlogArticleShell';

export default function Article() {
  const html = fs.readFileSync(
    path.join(process.cwd(), 'content/blog-articles', ${slugJson}, 'body.html'),
    'utf8'
  );
  return (
    <BlogArticleShell>
      <div dangerouslySetInnerHTML={{ __html: html }} />
    </BlogArticleShell>
  );
}
`;
}

function metaTemplate(slug, data, filename) {
  let dateStr = '';
  const raw = data.date;
  if (raw instanceof Date && !Number.isNaN(raw.getTime())) {
    dateStr = raw.toISOString().slice(0, 10);
  } else if (typeof raw === 'string' && /^\d{4}-\d{2}-\d{2}/.test(raw)) {
    dateStr = raw.slice(0, 10);
  } else {
    const m = filename.match(/^(\d{4}-\d{2}-\d{2})/);
    if (m) dateStr = m[1];
  }
  return `export const meta = {
  slug: ${JSON.stringify(slug)},
  title: ${JSON.stringify(data.title || '')},
  description: ${JSON.stringify(data.description || '')},
  date: ${JSON.stringify(dateStr)},
  keywords: ${JSON.stringify(data.keywords != null ? String(data.keywords) : '')},
  permalink: ${JSON.stringify(data.permalink != null ? String(data.permalink) : '')},
} as const;
`;
}

function metaDateFromFile(data, filename) {
  const raw = data.date;
  if (raw instanceof Date && !Number.isNaN(raw.getTime())) {
    return raw.toISOString().slice(0, 10);
  }
  if (typeof raw === 'string' && /^\d{4}-\d{2}-\d{2}/.test(raw)) {
    return raw.slice(0, 10);
  }
  const m = filename.match(/^(\d{4}-\d{2}-\d{2})/);
  if (m) return m[1];
  return '1970-01-01';
}

async function main() {
  if (!fs.existsSync(POSTS)) {
    console.error('Missing content/posts directory.');
    process.exit(1);
  }
  const files = fs.readdirSync(POSTS).filter((f) => f.endsWith('.md'));
  if (files.length === 0) {
    console.warn('No .md files in content/posts — nothing to migrate. (Blog content lives under content/blog-articles.)');
    process.exit(0);
  }
  const entries = [];

  for (const file of files) {
    const slug = slugFromFilename(file);
    const raw = fs.readFileSync(path.join(POSTS, file), 'utf8');
    const { data, content } = matter(raw);
    const html = await mdToHtml(content);
    const dir = path.join(OUT, slug);
    fs.mkdirSync(dir, { recursive: true });
    if (!CUSTOM_ARTICLE_SLUGS.has(slug)) {
      fs.writeFileSync(path.join(dir, 'body.html'), html.trimEnd() + '\n', 'utf8');
      fs.writeFileSync(path.join(dir, 'Article.tsx'), articleTemplate(slug), 'utf8');
    }
    fs.writeFileSync(path.join(dir, 'meta.ts'), metaTemplate(slug, data, file), 'utf8');

    entries.push({
      slug,
      date: metaDateFromFile(data, file),
      ident: safeIdent(slug),
    });
  }

  entries.sort((a, b) => b.date.localeCompare(a.date));

  const lines = [
    '/** Auto-generated by scripts/migrate-md-to-blog-articles.mjs — edit article folders under content/blog-articles only. */',
    '',
  ];
  for (const e of entries) {
    lines.push(`import Article_${e.ident} from './${e.slug}/Article';`);
    lines.push(`import { meta as meta_${e.ident} } from './${e.slug}/meta';`);
  }
  lines.push('');
  lines.push('export const blogArticleEntries = [');
  for (const e of entries) {
    lines.push(`  { meta: meta_${e.ident}, Article: Article_${e.ident} },`);
  }
  lines.push('] as const;');
  lines.push('');
  fs.writeFileSync(path.join(OUT, 'index.ts'), lines.join('\n'), 'utf8');

  console.log(`Wrote ${entries.length} articles to content/blog-articles/`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
