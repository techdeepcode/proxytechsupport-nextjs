/**
 * One-time / maintenance: convert content/interviews/*.md → content/interview-articles/<slug>/
 * Preserves full filename (including YYYY-MM-DD) as slug so /interviews/{slug}/ URLs stay identical.
 * Run from app root: node scripts/migrate-md-to-interview-articles.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import matter from 'gray-matter';
import { remark } from 'remark';
import remarkGfm from 'remark-gfm';
import remarkHtml from 'remark-html';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const POSTS = path.join(ROOT, 'content/interviews');
const OUT = path.join(ROOT, 'content/interview-articles');

function safeIdent(slug) {
  return slug.replace(/-/g, '_');
}

async function mdToHtml(mdBody) {
  const file = await remark().use(remarkGfm).use(remarkHtml).process(mdBody);
  return String(file);
}

function articleTemplate(slug) {
  const slugJson = JSON.stringify(slug);
  return `import { loadInterviewBodyHtml } from '@/lib/interview-body-html';

export default function Article() {
  const html = loadInterviewBodyHtml(${slugJson});
  return <div dangerouslySetInnerHTML={{ __html: html }} />;
}
`;
}

function metaTemplate(slug, data, filename) {
  let dateStr = '';
  const raw = data.date;
  if (raw instanceof Date && !Number.isNaN(raw.getTime())) {
    dateStr = raw.toISOString().slice(0, 10);
  } else if (typeof raw === 'string' && /^\d{4}-\d{2}-\d{2}/.test(raw)) {
    dateStr = raw.slice(0, 10);
  } else {
    const m = filename.match(/^(\d{4}-\d{2}-\d{2})/);
    if (m) dateStr = m[1];
  }
  const layout =
    data.layout != null && String(data.layout).trim()
      ? String(data.layout).trim()
      : 'interview-post';
  return `export const meta = {
  slug: ${JSON.stringify(slug)},
  title: ${JSON.stringify(data.title || '')},
  description: ${JSON.stringify(data.description || '')},
  date: ${JSON.stringify(dateStr)},
  keywords: ${JSON.stringify(data.keywords != null ? String(data.keywords) : '')},
  layout: ${JSON.stringify(layout)},
} as const;
`;
}

function metaDateFromFile(data, filename) {
  const raw = data.date;
  if (raw instanceof Date && !Number.isNaN(raw.getTime())) {
    return raw.toISOString().slice(0, 10);
  }
  if (typeof raw === 'string' && /^\d{4}-\d{2}-\d{2}/.test(raw)) {
    return raw.slice(0, 10);
  }
  const m = filename.match(/^(\d{4}-\d{2}-\d{2})/);
  if (m) return m[1];
  return '1970-01-01';
}

async function main() {
  if (!fs.existsSync(POSTS)) {
    console.error('Missing content/interviews directory.');
    process.exit(1);
  }
  const files = fs.readdirSync(POSTS).filter((f) => f.endsWith('.md'));
  if (files.length === 0) {
    console.warn('No .md files in content/interviews — nothing to migrate.');
    process.exit(0);
  }
  const entries = [];

  for (const file of files) {
    const slug = file.replace(/\.md$/i, '');
    const raw = fs.readFileSync(path.join(POSTS, file), 'utf8');
    const { data, content } = matter(raw);
    const html = await mdToHtml(content);
    const dir = path.join(OUT, slug);
    fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(path.join(dir, 'body.html'), html.trimEnd() + '\n', 'utf8');
    fs.writeFileSync(path.join(dir, 'Article.tsx'), articleTemplate(slug), 'utf8');
    fs.writeFileSync(path.join(dir, 'meta.ts'), metaTemplate(slug, data, file), 'utf8');

    entries.push({
      slug,
      date: metaDateFromFile(data, file),
      ident: safeIdent(slug),
    });
  }

  entries.sort((a, b) => b.date.localeCompare(a.date));

  const lines = [
    '/** Auto-generated by scripts/migrate-md-to-interview-articles.mjs — edit folders under content/interview-articles only. */',
    '',
  ];
  for (const e of entries) {
    lines.push(`import Article_${e.ident} from './${e.slug}/Article';`);
    lines.push(`import { meta as meta_${e.ident} } from './${e.slug}/meta';`);
  }
  lines.push('');
  lines.push('export const interviewArticleEntries = [');
  for (const e of entries) {
    lines.push(`  { meta: meta_${e.ident}, Article: Article_${e.ident} },`);
  }
  lines.push('] as const;');
  lines.push('');
  fs.writeFileSync(path.join(OUT, 'index.ts'), lines.join('\n'), 'utf8');

  console.log(`Wrote ${entries.length} interviews to content/interview-articles/`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
